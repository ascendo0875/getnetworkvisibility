<?php

namespace FINNPartners\Theme\Block\Register;

use WpAdvanceCustomFieldsExtend\AbstractClass\CustomBlock;

class ResourcesList extends CustomBlock
{
    const NAME = 'resources-list';
    const FILENAME = 'resources-list';
    const TITLE = 'Resources List';

    const ICON = 'id';

    public function __construct()
    {
        $this->setName(self::NAME)
            ->setTitle(self::TITLE)
            ->setRenderTemplate(self::FILENAME . '.php')
            ->setKeywords(['FP'])
            ->setIcon(self::ICON)
            ->setSupports(['jsx' => true, 'align' => false]);

        parent::__construct();
    }

    /**
     * @param string $class
     * @return ResourcesList
     */
    public static function getInstance(string $class = ResourcesList::class): ResourcesList
    {
        return parent::getInstance($class); // TODO: Change the autogenerated stub
    }
}